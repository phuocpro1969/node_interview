// linked list doesn't use physical placement of 
// elements of data memory, use a reference system

// 1 Node stored elements and contain a pointer 
// to the next node in the linked list

// this system allows efficient insertion and 
// removal of items without the need for 
// reorganization (ngoài việc cần tái cấu trúc)

// Advantages
// + Efficient(hiệu quả) insertion and
//   removal of new elements
// + Less complex than restructuring an array

// Disadvantages
// + Uses more memory than arrays
// + Inefficient to retrieve(phục hồi) 
//   a specific element
// + Inefficient to traverse the list backward